def build_app(exemplar_path: pathlib.Path) -> list[pathlib.Path]:
    if not exemplar_path.exists():
        sys.stderr.write(f"ERROR: exemplar UC not found: {exemplar_path}\n")
        raise SystemExit(2)

    uc = json.loads(exemplar_path.read_text(encoding="utf-8"))
    ucs = [uc]

    if APP_ROOT.exists():
        shutil.rmtree(APP_ROOT)
    default_dir = APP_ROOT / "default"
    views_dir = default_dir / "data" / "ui" / "views"
    nav_dir = default_dir / "data" / "ui" / "nav"
    meta_dir = APP_ROOT / "metadata"
    for d in (default_dir, views_dir, nav_dir, meta_dir):
        d.mkdir(parents=True, exist_ok=True)

    write_app_conf(default_dir / "app.conf", len(ucs))
    write_manifest(APP_ROOT / "app.manifest", len(ucs))
    write_savedsearches(default_dir / "savedsearches.conf", ucs)
    write_nav(nav_dir / "default.xml")
    write_view(views_dir / "compliance_overview.xml", uc)
    write_meta(meta_dir / "default.meta")
    write_readme(APP_ROOT / "README.md", ucs)

    files = sorted(
        p for p in APP_ROOT.rglob("*") if p.is_file()
    )
    return files


def shape_check(files: list[pathlib.Path]) -> list[str]:
    """Minimal static checks that AppInspect will otherwise catch."""
    errors: list[str] = []
    required = {
        APP_ROOT / "default" / "app.conf",
        APP_ROOT / "default" / "savedsearches.conf",
        APP_ROOT / "default" / "data" / "ui" / "nav" / "default.xml",
        APP_ROOT / "default" / "data" / "ui" / "views" / "compliance_overview.xml",
        APP_ROOT / "metadata" / "default.meta",
        APP_ROOT / "app.manifest",
        APP_ROOT / "README.md",
    }
    present = set(files)
    for req in required:
        if req not in present:
            errors.append(f"missing required file: {req.relative_to(REPO)}")

    manifest_path = APP_ROOT / "app.manifest"
    if manifest_path in present:
        try:
            manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
            if manifest.get("schemaVersion") != "2.0.0":
                errors.append("app.manifest schemaVersion must be 2.0.0")
            info = manifest.get("info", {})
            if info.get("id", {}).get("name") != APP_ID:
                errors.append(f"app.manifest info.id.name must be {APP_ID}")
        except json.JSONDecodeError as exc:
            errors.append(f"app.manifest not valid JSON: {exc}")

    nav_path = APP_ROOT / "default" / "data" / "ui" / "nav" / "default.xml"
    if nav_path in present:
        try:
            ET.parse(nav_path)
        except ET.ParseError as exc:
            errors.append(f"nav/default.xml not valid XML: {exc}")

    view_path = APP_ROOT / "default" / "data" / "ui" / "views" / "compliance_overview.xml"
    if view_path in present:
        try:
            ET.parse(view_path)
        except ET.ParseError as exc:
            errors.append(f"views/compliance_overview.xml not valid XML: {exc}")

    return errors


def main() -> int:
    files = build_app(EXEMPLAR)
    errors = shape_check(files)
    if errors:
        for err in errors:
            sys.stderr.write(f"FAIL: {err}\n")
        return 1

    contents_hash = hashlib.sha256()
    for path in files:
        rel = path.relative_to(APP_ROOT).as_posix().encode("utf-8")
        contents_hash.update(rel + b"\0" + path.read_bytes() + b"\0")

    print(
        f"PASS: generated {len(files)} file(s) under {APP_ROOT.relative_to(REPO)}\n"
        f"      sha256 (tree) : {contents_hash.hexdigest()}"
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
