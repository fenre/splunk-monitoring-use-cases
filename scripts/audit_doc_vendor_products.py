def load_equipment_allowlist() -> set[str]:
    if not EQUIPMENT_INDEX.is_file():
        return set()
    data = json.loads(EQUIPMENT_INDEX.read_text(encoding="utf-8"))
    out: set[str] = set()
    for entry in data.get("equipment", []):
        label = (entry.get("label") or "").strip()
        if label:
            out.add(label.lower())
        for model in entry.get("models", []):
            ml = (model.get("label") or "").strip()
            if ml:
                out.add(ml.lower())
    return out


def load_splunkbase_allowlist() -> set[str]:
    if not SPLUNKBASE_CATALOG.is_file():
        return set()
    try:
        data = json.loads(SPLUNKBASE_CATALOG.read_text(encoding="utf-8"))
    except Exception:
        return set()
    out: set[str] = set()
    # Catalog shape: {"apps": [{"id":"...","title":"..."}, ...]}
    apps = data.get("apps") or data.get("entries") or []
    if isinstance(apps, dict):
        apps = apps.values()
    for entry in apps:
        if not isinstance(entry, dict):
            continue
        title = (
            entry.get("title") or entry.get("name") or entry.get("label") or ""
        ).strip()
        if title:
            out.add(title.lower())
    return out


def build_allowlist() -> dict[str, set[str]]:
    """Return {vendor_anchor: {lower-case product phrase, ...}}."""
    equipment = load_equipment_allowlist()
    splunkbase = load_splunkbase_allowlist()

    # All entries are LOWER-cased; matching is case-insensitive but
    # preserves the original anchor case in reports.
    cisco = set(CURATED_CISCO)
    splunk = set(CURATED_SPLUNK)
    # Add equipment labels that start with a known vendor.
    for label in equipment:
        if label.startswith("cisco"):
            cisco.add(label)
        if label.startswith("splunk"):
            splunk.add(label)
    # Splunkbase apps are mostly "<Vendor> Add-on for X" — we use
    # them only to enrich the Splunk allowlist, not as gospel.
    for app_title in splunkbase:
        if app_title.startswith("splunk"):
            splunk.add(app_title)

    return {
        "Cisco": cisco,
        "Splunk": splunk,
    }


# ---------------------------------------------------------------- audit
#
# The audit walks every document, extracts vendor+product phrases,
# and emits two reports:
#
#   - mentions: every observed (anchor, phrase) with counts and
#     source documents. Useful for a human eyeball pass.
#   - suspicious: phrases for vendors with an allowlist (Cisco /
#     Splunk) that did NOT match the allowlist and are NOT in
#     KNOWN_PROSE_NOISE. Candidates for hallucination, not
#     proof of hallucination.

def audit(docs: list[Path]) -> dict:
    allowlists = build_allowlist()
    mentions: dict[str, dict] = defaultdict(
        lambda: {"count": 0, "files": defaultdict(int)}
    )
    by_anchor: dict[str, int] = defaultdict(int)

    for p in docs:
        rel = str(p.relative_to(REPO)).replace("\\", "/")
        try:
            text = p.read_text(encoding="utf-8", errors="replace")
        except Exception:
            continue
        seen = set()
        for anchor, phrase, _ in extract_phrases(text):
            key = (anchor, phrase)
            seen.add(key)
            mentions[phrase]["anchor"] = anchor
            mentions[phrase]["count"] += 1
            mentions[phrase]["files"][rel] += 1
            by_anchor[anchor] += 1

    # Sort + tidy for serialisation.
    mentions_sorted = sorted(
        mentions.items(), key=lambda kv: (-kv[1]["count"], kv[0])
    )
    serial_mentions = []
    for phrase, rec in mentions_sorted:
        serial_mentions.append({
            "anchor": rec["anchor"],
            "phrase": phrase,
            "count": rec["count"],
            "files": dict(sorted(rec["files"].items(), key=lambda kv: -kv[1])),
        })

    # Build the suspicious list (Cisco / Splunk only for now).
    suspicious = []
    for entry in serial_mentions:
        anchor = entry["anchor"]
        if anchor not in allowlists:
            continue
        phrase_lower = entry["phrase"].lower()
        if phrase_lower in KNOWN_PROSE_NOISE:
            continue
        if phrase_lower in allowlists[anchor]:
            continue
        # Accept any allowlist entry that the phrase STARTS with — but
        # ONLY when the entry is multi-token (>= anchor + 1 product
        # part) and the next character after the entry is a space.
        # Without these guards, a 1-word entry like "cisco" would
        # silently absolve every "Cisco Foo Bar" extraction.
        if any(
            len(allow.split()) >= 2
            and phrase_lower.startswith(allow + " ")
            for allow in allowlists[anchor]
        ):
            continue
        suspicious.append(entry)

    return {
        "_meta": {
            "schemaVersion": "1.0",
            "description": (
                "Vendor / product-name mentions extracted from the "
                "prose docs corpus. Phase A (mentions) is exhaustive; "
                "Phase B (suspicious) flags Cisco / Splunk phrases "
                "that do not match the curated allowlist."
            ),
        },
        "anchor_counts": dict(
            sorted(by_anchor.items(), key=lambda kv: -kv[1])
        ),
        "mentions": serial_mentions,
        "suspicious": suspicious,
    }


# ---------------------------------------------------------------- CLI

def _print_summary(payload: dict, top: int) -> None:
    print()
    print(f"Vendor anchors:")
    for anchor, n in payload["anchor_counts"].items():
        print(f"  {anchor:<14s}  {n:>5d} mentions")
    susp = payload["suspicious"]
    print()
    print(f"Phase B — {len(susp)} suspicious phrases "
          f"(allowlist miss, Cisco / Splunk)")
    for entry in susp[:top]:
        print(f"  {entry['count']:>3d}  {entry['phrase']}")
        for fp, n in list(entry["files"].items())[:3]:
            print(f"        {n:>3d}× {fp}")


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--top", type=int, default=40,
                        help="Number of suspicious phrases to print "
                             "(default 40).")
    parser.add_argument("--report", action="store_true",
                        help="Read existing data/doc-vendor-mentions.json "
                             "and print the summary without re-scanning.")
    parser.add_argument(
        "--strict",
        action="store_true",
        help="Exit 1 if any suspicious phrases are found. Intended "
             "for CI once the allowlist is exhaustive.",
    )
    args = parser.parse_args(argv)

    if args.report:
        if not STATUS_PATH.is_file():
            print(f"No status file at {STATUS_PATH}.")
            return 2
        payload = json.loads(STATUS_PATH.read_text(encoding="utf-8"))
    else:
        docs = collect_docs()
        if not docs:
            print("No prose docs found under docs/.")
            return 2
        print(f"Scanning {len(docs)} markdown files ...")
        payload = audit(docs)
        STATUS_PATH.parent.mkdir(parents=True, exist_ok=True)
        STATUS_PATH.write_text(
            json.dumps(payload, indent=2, ensure_ascii=False) + "\n",
            encoding="utf-8",
        )

    _print_summary(payload, args.top)
    if args.strict and payload.get("suspicious"):
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main())
