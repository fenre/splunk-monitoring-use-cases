# Build a flat anchor list once. Multi-word anchors (e.g. "Red Hat",
# "Palo Alto", "Check Point") have to be matched as a single unit so
# we don't mis-attribute the trailing token as the "product".
ANCHORS_SORTED = sorted(VENDOR_SURFACES.keys(), key=len, reverse=True)

# ---------------------------------------------------------------- tail tokenisation
#
# A "product tail" is up to N CAPITALISED tokens that immediately
# follow the vendor anchor. Stop conditions:
#
# - Lower-case word (English connector or sentence continuation).
# - Punctuation (sentence boundary).
# - End of line.
# - Another vendor anchor (the writer pivoted to a new vendor).
#
# Capitalised here = starts with [A-Z] OR is fully uppercase (acronym)
# OR contains a digit (e.g. "9300", "ASA5500", "IOS-XE"). Single-letter
# trailing initials are allowed (e.g. "Splunk D Universal Forwarder").

CAPITALISED_TOKEN_RE = re.compile(
    # A capitalised "product-name word" — letter-start words (e.g.
    # "Catalyst"), all-caps acronyms (e.g. "ASA", "ESA"), and
    # alpha-leading version qualifiers (e.g. "v9", "X.Y"). Purely
    # numeric tokens are NOT accepted here because the walker would
    # otherwise gobble random version numbers next to the vendor
    # name ("Splunk 9.4", "Splunk 17", "Splunk 134") as if they
    # were product names.
    r"[A-Z][A-Za-z0-9][\w./+\-]*"
    r"|[A-Z]{2,}"
)
ENGLISH_STOPWORDS = {
    "and", "or", "the", "a", "an", "of", "for", "with", "to",
    "in", "on", "from", "by", "as", "at", "is", "are", "was",
    "were", "be", "been", "being", "this", "that", "these",
    "those", "but", "if", "then", "else", "than", "when",
    "while", "where", "via", "into", "onto", "over", "under",
    "across", "between", "per", "vs", "versus", "such",
}
# Tail tokens that look like product-name words but are nearly
# always English prose verbs / nouns the walker has mis-classified.
# Lower-case for membership testing.
PROSE_VERB_BLACKLIST = {
    "added", "adds", "alerts", "blogs", "checks", "combine",
    "connection", "correlate", "detectors", "docs", "documentation",
    "done", "fits", "five", "four", "generated", "guide",
    "guides", "include", "includes", "install", "installed",
    "integration", "integrations", "itself", "logging", "most",
    "official", "okay", "pages", "patterns", "platform", "plus",
    "provides", "review", "see", "settings", "shows", "store",
    "supports", "team", "teams", "three", "two", "umbrella",
    "use", "useful", "vendor", "vendors", "what", "you",
    "yes", "yourself",
}


def tokenise(text: str) -> list[tuple[str, int]]:
    """Return [(token, char_offset)] for one cleaned-prose document.

    We keep punctuation and case intact so the tail-walker can stop
    on a comma or a lower-case word."""
    return [(m.group(0), m.start()) for m in re.finditer(r"\S+", text)]


# Each token may carry trailing punctuation. Strip it for matching
# but record the original so the boundary detector still sees it.
TRAILING_PUNCT = ".,;:!?)]}\u201d\u2019\u2014\u2013"  # ASCII + curly + dashes


def is_capitalised(token: str) -> bool:
    """Return True for a token that's a plausible product-name word."""
    bare = token.rstrip(TRAILING_PUNCT)
    if not bare:
        return False
    return bool(CAPITALISED_TOKEN_RE.fullmatch(bare))


def is_terminating_punct(token: str) -> bool:
    """Return True if the token ends a clause (so the tail must stop)."""
    return token and token[-1] in ".,;:!?)]"


def normalise_phrase(words: list[str]) -> str:
    """Strip trailing punctuation from the last word and collapse spaces."""
    if not words:
        return ""
    out = list(words)
    out[-1] = out[-1].rstrip(TRAILING_PUNCT)
    return " ".join(out).strip()


# ---------------------------------------------------------------- extraction
#
# The walker is intentionally conservative — it errs on the side of
# emitting fewer, higher-confidence phrases. A few real product
# mentions WILL be missed; that's preferable to a noise-flooded
# allowlist.

def extract_phrases(text: str) -> list[tuple[str, str, int]]:
    """Return [(anchor, phrase, char_offset)] for one document."""
    cleaned = clean_prose(text)
    tokens = tokenise(cleaned)
    out: list[tuple[str, str, int]] = []

    i = 0
    while i < len(tokens):
        token, offset = tokens[i]
        # Look for the longest anchor starting at position i.
        anchor = None
        anchor_len = 0
        for cand in ANCHORS_SORTED:
            parts = cand.split()
            if i + len(parts) > len(tokens):
                continue
            if all(
                tokens[i + k][0].rstrip(TRAILING_PUNCT) == parts[k]
                for k in range(len(parts))
            ):
                anchor = cand
                anchor_len = len(parts)
                break
        if not anchor:
            i += 1
            continue

        # Walk the tail.
        max_tail = VENDOR_SURFACES[anchor]
        tail: list[str] = []
        j = i + anchor_len
        while j < len(tokens) and len(tail) < max_tail:
            t, _ = tokens[j]
            bare = t.rstrip(TRAILING_PUNCT)
            if not bare:
                break
            if bare.lower() in ENGLISH_STOPWORDS:
                break
            if bare.lower() in PROSE_VERB_BLACKLIST:
                # Vendor-prefixed prose ("Splunk provides…",
                # "Cisco offers…"); not a product name.
                break
            if not is_capitalised(t):
                break
            # Tokens with "/" are nearly always slash-separated
            # alternatives ("Cisco IOS/NX-OS/IOS-XR" — three OSes
            # listed inline, not a single product) or vendor-chains
            # ("Splunk Stealthwatch/Securonix"). Stop the tail at
            # the slash rather than emit a Frankenphrase.
            if "/" in bare:
                break
            # UC-X.Y.Z references are catalogue IDs, not products.
            if re.fullmatch(r"UC-\d+\.\d+\.\d+", bare):
                break
            if bare in ANCHORS_SORTED:
                break
            tail.append(t)
            if is_terminating_punct(t):
                # Token had a comma/period — accept this token but
                # do not extend further.
                break
            j += 1

        if not tail:
            i = i + anchor_len
            continue

        phrase = anchor + " " + normalise_phrase(tail)
        out.append((anchor, phrase, offset))
        i = j
    return out


