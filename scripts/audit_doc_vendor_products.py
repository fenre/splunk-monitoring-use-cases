#!/usr/bin/env python3
"""
audit_doc_vendor_products.py — vendor / product-name hallucination
sweep across the repository's prose documentation.

Why this script exists
----------------------

URL audits (scripts/audit_doc_urls.py) catch dead links, but a
fully resolvable URL can still sit next to a hallucinated *product
name* — "Cisco Catalyst Center Identity Services Engine" (a
real-sounding mash-up that does not exist) or "Splunk Real Time
Observability Suite" (no such product). This script extracts every
"Vendor + 1–4 capitalised tokens" phrase from the prose docs and:

1. **Phase A — Frequency report**
   Groups by phrase, counts occurrences across the corpus, and
   lists the source files. Human review tags suspicious phrases.

2. **Phase B — Targeted allowlist check** (Cisco / Splunk)
   Cross-references each phrase against an allowlist seeded from
   `api/v1/equipment/index.json` plus a curated baseline of
   well-known product names. Anything outside the allowlist is
   flagged for review as a *candidate* hallucination — false
   positives are expected, so the script is **report-only**.

Outputs
-------

Writes `data/doc-vendor-mentions.json` (machine-readable, used by
follow-up audits and reviews) and prints a human summary to stdout.

What it does NOT do
-------------------

- Does not modify any markdown. Fix-ups belong in a separate
  curated pass once human review has tagged the truly bad mentions.
- Does not probe vendor sites to validate product names. The
  allowlist is the source of truth; expand it as legitimate
  products surface.
- Skips fenced code blocks, inline code, the auto-generated
  references footer, and the YAML front-matter `product_aliases`
  block (which is intentionally a sprawling SEO keyword list,
  not prose claims about reality).

Stdlib-only. No third-party dependencies.
"""
