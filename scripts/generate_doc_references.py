_ACCESSED_DATE_RESOLVED: str | None = None


def _resolve_accessed_date(library_accessed: str | None) -> str:
    """Resolve and cache the long-form retrieval date.

    Priority: SOURCE_REFS_ACCESSED_DATE env var > _meta.accessedDate >
    today's UTC date.
    """
    global _ACCESSED_DATE_RESOLVED
    if _ACCESSED_DATE_RESOLVED is not None:
        return _ACCESSED_DATE_RESOLVED
    raw = ACCESSED_DATE_ENV or library_accessed
    if raw:
        try:
            d = dt.date.fromisoformat(raw)
        except ValueError:
            d = dt.datetime.utcnow().date()
    else:
        d = dt.datetime.utcnow().date()
    fmt = "%B %-d, %Y" if sys.platform != "win32" else "%B %#d, %Y"
    _ACCESSED_DATE_RESOLVED = d.strftime(fmt)
    return _ACCESSED_DATE_RESOLVED


def format_accessed_date() -> str:
    """Long-form retrieval date like 'May 11, 2026'."""
    return _resolve_accessed_date(None)


def render_citation(idx: int, rec: dict) -> str:
    """Render one bibliographic record in APA-flavoured form.

    Output shape:
        [N] Authority. (Year). *Title* (Edition). Publisher.
            Identifier. Retrieved <date> from URL
    """
    parts: list[str] = []
    authority = rec.get("authority", "").strip()
    year = rec.get("year")
    month = rec.get("month")
    title = (rec.get("title") or "").strip()
    publisher = rec.get("publisher", "").strip()
    edition = rec.get("edition", "").strip()
    identifier = rec.get("identifier", "").strip()
    url = rec.get("url", "").strip()
    rec_type = rec.get("type", "documentation")

    def _terminated(s: str) -> str:
        s = s.rstrip()
        if s and s[-1] not in ".?!":
            s += "."
        return s

    # Authority (Year[, Month]).
    if authority:
        parts.append(_terminated(authority))
    if year:
        if month:
            try:
                m_name = dt.date(int(year), int(month), 1).strftime("%B")
                parts.append(f"({year}, {m_name}).")
            except (TypeError, ValueError):
                parts.append(f"({year}).")
        else:
            parts.append(f"({year}).")
    # Title.
    if title:
        title_str = f"*{title.rstrip('.')}*"
        if edition:
            title_str += f" ({edition})"
        parts.append(title_str + ".")
    # Publisher (italics not needed; period separates).
    if publisher and publisher != authority:
        parts.append(_terminated(publisher))
    # Identifier (RFC #, ISBN, NIST SP, ELI…)
    if identifier:
        parts.append(_terminated(identifier))
    # Retrieval (for live web sources) or DOI/URL.
    if url:
        if rec_type in {"documentation", "guide", "reference", "specification",
                        "whitepaper"}:
            parts.append(f"Retrieved {format_accessed_date()}, from {url}")
        else:
            # Standards, regulations, books — URL is canonical, no retrieval.
            parts.append(url)

    body = " ".join(parts).strip()
    return f'<a id="ref-{idx}"></a>**[{idx}]** {body}'


def render_section(
    src_rel: str,
    src_path: Path,
    library: dict[str, dict],
    mappings: dict[str, dict],
    keywords: dict[str, list[str]],
    out_links: set[str],
    in_links: set[str],
) -> tuple[str, dict[str, int]]:
    """Render the references footer and return (footer_text, id→number map).

    The id→number map lets the inline-citation pass look up which
    reference number a source-ID was assigned in *this* doc.
    """
    raw = src_path.read_text(encoding="utf-8", errors="replace")
    body = strip_code_regions(strip_autogen_block(raw))

    mapping = get_doc_mapping(src_rel, mappings)
    primary_ids = list(mapping.get("primary", []))
    supporting_ids = list(mapping.get("supporting", []))
    related_ids = list(mapping.get("related", []))

    # Auto-resolve more supporting IDs from keyword scan (capped).
    explicit = set(primary_ids) | set(supporting_ids) | set(related_ids)
    auto_hits = [sid for sid in auto_resolve_keywords(body, keywords)
                 if sid not in explicit]
    auto_added = auto_hits[:MAX_AUTO_SUPPORTING]

    # Validate IDs exist in the library; drop missing with a warning.
    def resolve(ids: list[str]) -> list[dict]:
        out: list[dict] = []
        seen = set()
        for sid in ids:
            if sid in seen:
                continue
            seen.add(sid)
            rec = library.get(sid)
            if rec is None:
                # Silent skip; a missing ID is reported separately by --check.
                continue
            out.append(rec)
        return out

    primary_records = resolve(primary_ids)
    supporting_records = resolve(supporting_ids + auto_added)
    # If the same ID appears in primary and supporting, drop from supporting.
    primary_set = {r["_id"] for r in primary_records}
    supporting_records = [r for r in supporting_records
                          if r["_id"] not in primary_set]
    related_records = [r for r in resolve(related_ids)
                       if r["_id"] not in primary_set
                       and r["_id"] not in {x["_id"] for x in supporting_records}]

    # Sort Supporting / Related sections alphabetically by authority then
    # title. (Primary is kept in author-chosen order so the foremost
    # source comes first.) This produces a stable, bibliography-style
    # ordering rather than first-mapped-wins.
    def _sort_key(r: dict) -> tuple[str, str]:
        # Normalise "European Parliament and Council of the European Union"
        # to behave like a single author surname for ordering.
        a = (r.get("authority") or "").lower()
        a = re.sub(r"^(the|a|an)\s+", "", a)
        # Strip honorifics / corporate suffixes for a cleaner sort.
        a = re.sub(r"\b(inc|llc|ltd|corp|corporation|company|gmbh|sa|nv)\.?\b",
                   "", a).strip()
        return (a, (r.get("title") or "").lower())

    supporting_records = sorted(supporting_records, key=_sort_key)
    related_records = sorted(related_records, key=_sort_key)

    # Track URLs covered by curated records so we don't double-list.
    covered: set[str] = set()
    for r in (*primary_records, *supporting_records, *related_records):
        if r.get("url"):
            covered.add(urldefrag(r["url"]).url)

    additional = extract_additional_urls(body, covered)

    # Number sequentially across all three buckets.
    lines: list[str] = []
    lines.append(BEGIN_MARKER)
    lines.append("")
    lines.append(SECTION_HEADING)
    lines.append("")
    lines.append(
        f"*Auto-generated by `scripts/generate_doc_references.py` from "
        f"`data/source-references.json` and `data/source-mappings.json`. "
        f"Edit those files (or the document body) to change citations; "
        f"this footer is rewritten on every run.*"
    )
    lines.append("")

    idx = 1
    id_to_num: dict[str, int] = {}

    def emit_record_list(heading: str, records: list[dict]) -> None:
        nonlocal idx
        if not records:
            return
        lines.append(heading)
        lines.append("")
        for r in records:
            lines.append(render_citation(idx, r))
            lines.append("")
            id_to_num[r["_id"]] = idx
            idx += 1

    emit_record_list(SUBSECTION_PRIMARY, primary_records)
    emit_record_list(SUBSECTION_SUPPORTING, supporting_records)

    if additional:
        # Filler "extra body URLs" — useful but not editorially curated.
        # Demote into a collapsed <details> fold so they don't outshout
        # the real bibliography. Drop the section entirely when the doc
        # already has plenty of curated refs and the extras are mostly
        # noise.
        skip_additional = (
            len(primary_records) + len(supporting_records) >= 12
            and len(additional) <= 2
        )
        if not skip_additional:
            lines.append("<details>")
            lines.append(
                "<summary>"
                f"Additional online sources cited in the document body "
                f"({len(additional)})"
                "</summary>"
            )
            lines.append("")
            for url, label in additional.items():
                rec = {
                    "title": label,
                    "authority": urlparse(url).netloc.removeprefix("www."),
                    "year": None,
                    "type": "reference",
                    "url": url,
                }
                lines.append(render_citation(idx, rec))
                lines.append("")
                idx += 1
            lines.append("</details>")
            lines.append("")

    # Internal repo cross-refs at the end (kept brief).
    def link_from(src_rel: str, target_rel: str) -> str:
        src_dir = (REPO / src_rel).parent.resolve()
        return os.path.relpath((REPO / target_rel).resolve(), src_dir).replace(os.sep, "/")

    out_sorted = sorted(out_links)
    in_sorted = sorted(in_links)
    if out_sorted:
        lines.append(SUBSECTION_REPO_OUT)
        lines.append("")
        for t in out_sorted[:MAX_REPO_LINKS_PER_DIRECTION]:
            lines.append(f"- [`{t}`]({link_from(src_rel, t)})")
        if len(out_sorted) > MAX_REPO_LINKS_PER_DIRECTION:
            lines.append(f"- _… and {len(out_sorted) - MAX_REPO_LINKS_PER_DIRECTION} "
                         f"more (see [`docs/backlinks.md`]"
                         f"({link_from(src_rel, 'docs/backlinks.md')}))._")
        lines.append("")
    if in_sorted:
        lines.append(SUBSECTION_REPO_IN)
        lines.append("")
        for s in in_sorted[:MAX_REPO_LINKS_PER_DIRECTION]:
            lines.append(f"- [`{s}`]({link_from(src_rel, s)})")
        if len(in_sorted) > MAX_REPO_LINKS_PER_DIRECTION:
            lines.append(f"- _… and {len(in_sorted) - MAX_REPO_LINKS_PER_DIRECTION} "
                         f"more (see [`docs/backlinks.md`]"
                         f"({link_from(src_rel, 'docs/backlinks.md')}))._")
        lines.append("")

    if idx == 1 and not (out_sorted or in_sorted):
        lines.append(
            "_No external sources are cited inline in this document and "
            "no repository documents currently link to it._"
        )
        lines.append("")

    lines.append(END_MARKER)
    return "\n".join(lines), id_to_num


