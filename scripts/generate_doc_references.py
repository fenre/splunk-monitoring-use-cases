# ─────────────────────────────────────────────────────────────────────────
# Helpers — code/marker stripping, URL hygiene, label cleanup
# ─────────────────────────────────────────────────────────────────────────


def strip_code_regions(text: str) -> str:
    lines = text.split("\n")
    in_fence = False
    out: list[str] = []
    for line in lines:
        if re.match(r"^[ \t]*```", line):
            in_fence = not in_fence
            out.append("")
            continue
        out.append("" if in_fence else line)
    body = "\n".join(out)
    body = re.sub(r"`[^`\n]*`", "", body)
    return body


def strip_autogen_block(text: str) -> str:
    """Strip our own footer so the body scan never feeds back on itself."""
    return MARKER_BLOCK_RE.sub("", text)


def url_excluded(url: str) -> bool:
    return any(re.match(pat, url) for pat in URL_EXCLUDE_PATTERNS)


def normalize_url(url: str) -> str:
    """Strip trailing punctuation that markdown / prose tends to attach."""
    url = url.rstrip(".,;:!?\"'>")
    while url.endswith(")") and url.count("(") < url.count(")"):
        url = url[:-1]
    return url


def humanise_url(url: str, library_titles: dict[str, str] | None = None) -> str:
    """Best-effort human-readable title for an uncurated URL.

    Falls back ONLY when no curated record covers this URL. Output is
    never a bare slug — we always produce a multi-word descriptor.
    """
    parsed = urlparse(url)
    host = parsed.netloc.lower()
    path = parsed.path.rstrip("/")
    fragment = parsed.fragment

    # Avoid producing weak single-slug labels: package the host as
    # publisher context in every case.
    def humanise_segment(seg: str) -> str:
        seg = re.sub(r"\.html?$", "", seg or "")
        seg = re.sub(r"[_\-]+", " ", seg)
        seg = re.sub(r"([a-z])([A-Z])", r"\1 \2", seg)
        return seg.strip().title()

    last = path.split("/")[-1] if path else ""

    # MITRE ATT&CK and friends
    if "attack.mitre.org" in host:
        m = re.search(r"/techniques/(T\d+(?:\.\d+)?)", path)
        if m:
            return f"MITRE ATT&CK Technique {m.group(1)}"
        m = re.search(r"/tactics/(TA\d+)", path)
        if m:
            return f"MITRE ATT&CK Tactic {m.group(1)}"
        m = re.search(r"/(groups|software|campaigns)/([A-Z0-9]+)", path)
        if m:
            return f"MITRE ATT&CK {m.group(1).rstrip('s').title()} {m.group(2)}"
        return "MITRE ATT&CK Knowledge Base"
    if "d3fend.mitre.org" in host:
        return f"MITRE D3FEND ({humanise_segment(last) or 'index'})"

    # RFCs
    m = re.search(r"rfc[-/]?(\d+)", path, re.IGNORECASE)
    if m and ("rfc-editor.org" in host or "ietf.org" in host or "tools.ietf.org" in host):
        return f"IETF RFC {m.group(1)}"

    # Splunkbase
    if "splunkbase.splunk.com" in host:
        m = re.search(r"/app/(\d+)", path)
        if m:
            return f"Splunkbase app #{m.group(1)}"
        return "Splunkbase"

    # Splunk docs
    if "docs.splunk.com" in host:
        # /Documentation/<Product>/<Version>/<Manual>/<Page>
        m = re.search(r"/Documentation/([^/]+)(?:/([^/]+))?(?:/([^/]+))?(?:/([^/]+))?", path)
        if m:
            prod, ver, manual, page = m.group(1), m.group(2), m.group(3), m.group(4)
            tail = humanise_segment(page or manual or "")
            ver_tag = f" {ver}" if ver and ver not in ("latest", "Latest") else ""
            return f"Splunk {prod}{ver_tag}: {tail}".strip(": ").rstrip()
        m = re.search(r"/observability/(?:en/)?([^/]+)?(?:/([^/]+))?", path)
        if m:
            section, page = m.group(1), m.group(2)
            tail = humanise_segment(page or section or "")
            return f"Splunk Observability: {tail}".strip(": ")
    if "lantern.splunk.com" in host:
        tail = humanise_segment(last) or "article"
        return f"Splunk Lantern: {tail}"

    # NIST publications
    if "csrc.nist.gov" in host or "nist.gov" in host:
        m = re.search(r"(SP\s*\d+-\d+[A-Z]?)", path + " " + fragment, re.IGNORECASE)
        if m:
            ident = m.group(1).upper().replace(" ", " ")
            return f"NIST {ident}"
        if "cyberframework" in path:
            return "NIST Cybersecurity Framework"
        return f"NIST: {humanise_segment(last) or 'publication'}"

    # ISO
    if "iso.org" in host:
        m = re.search(r"(\d{4,5}(?:-\d+)?)", path)
        if m:
            return f"ISO/IEC {m.group(1)}"
        return "ISO/IEC standard"

    # EU regulatory
    if "eur-lex.europa.eu" in host:
        m = re.search(r"/eli/(reg|dir|dec)/(\d{4})/(\d+)", path)
        if m:
            kind, year, num = m.groups()
            label = {"reg": "Regulation", "dir": "Directive", "dec": "Decision"}[kind]
            return f"EU {label} {year}/{num}"
        return f"EUR-Lex: {humanise_segment(last) or 'EU legal text'}"

    # Legislation.gov.uk
    if "legislation.gov.uk" in host:
        m = re.search(r"/(\w+)/(\d{4})/(\d+)", path)
        if m:
            return f"UK Legislation {m.group(2)} c. {m.group(3)}"
        return "UK Legislation: " + (humanise_segment(last) or "statute")

    # PCAOB / SEC / HHS
    if "pcaobus.org" in host:
        if "AS2201" in path:
            return "PCAOB AS 2201"
        return f"PCAOB: {humanise_segment(last) or 'document'}"
    if "sec.gov" in host:
        return f"U.S. SEC: {humanise_segment(last) or 'document'}"
    if "hhs.gov" in host:
        return f"U.S. HHS: {humanise_segment(last) or 'guidance'}"
    if "ftc.gov" in host:
        return f"U.S. FTC: {humanise_segment(last) or 'guidance'}"

    # AICPA
    if "aicpa" in host:
        return f"AICPA: {humanise_segment(last) or 'standard'}"

    # PCI SSC
    if "pcisecuritystandards.org" in host:
        return f"PCI SSC: {humanise_segment(last) or 'document'}"

    # OWASP
    if "owasp.org" in host or "cheatsheetseries.owasp.org" in host:
        return f"OWASP: {humanise_segment(last) or 'resource'}"

    # CISA
    if "cisa.gov" in host:
        return f"CISA: {humanise_segment(last) or 'advisory'}"

    # GitHub
    if "github.com" in host or "raw.githubusercontent.com" in host:
        parts = [p for p in path.split("/") if p]
        if len(parts) >= 2:
            return f"GitHub: {parts[0]}/{parts[1]}"
        if parts:
            return f"GitHub: {parts[0]}"
        return "GitHub"

    # Vendor platforms — produce "<Vendor>: <Page Title>"
    vendor_map: list[tuple[str, str]] = [
        ("learn.microsoft.com", "Microsoft Learn"),
        ("docs.microsoft.com", "Microsoft Docs"),
        ("azure.microsoft.com", "Microsoft Azure"),
        ("docs.aws.amazon.com", "AWS Documentation"),
        ("aws.amazon.com", "AWS"),
        ("cloud.google.com", "Google Cloud"),
        ("kubernetes.io", "Kubernetes"),
        ("docs.docker.com", "Docker"),
        ("redhat.com", "Red Hat"),
        ("access.redhat.com", "Red Hat Customer Portal"),
        ("paloaltonetworks.com", "Palo Alto Networks"),
        ("docs.paloaltonetworks.com", "Palo Alto Networks"),
        ("fortinet.com", "Fortinet"),
        ("docs.fortinet.com", "Fortinet"),
        ("vmware.com", "VMware"),
        ("docs.vmware.com", "VMware"),
        ("documentation.meraki.com", "Cisco Meraki"),
        ("cisco.com", "Cisco"),
        ("developer.cisco.com", "Cisco DevNet"),
        ("crowdstrike.com", "CrowdStrike"),
        ("falcon.crowdstrike.com", "CrowdStrike Falcon"),
        ("okta.com", "Okta"),
        ("developer.okta.com", "Okta Developer"),
        ("developer.hashicorp.com", "HashiCorp Developer"),
        ("hashicorp.com", "HashiCorp"),
        ("elastic.co", "Elastic"),
        ("grafana.com", "Grafana Labs"),
        ("prometheus.io", "Prometheus"),
        ("opentelemetry.io", "OpenTelemetry"),
        ("kubernetes.io", "Kubernetes"),
        ("docs.openshift.com", "OpenShift"),
        ("docs.citrix.com", "Citrix"),
        ("techdocs.f5.com", "F5"),
        ("f5.com", "F5"),
        ("docs.thousandeyes.com", "Cisco ThousandEyes"),
        ("modelcontextprotocol.io", "Model Context Protocol"),
        ("anthropic.com", "Anthropic"),
        ("coalitionforsecureai.org", "Coalition for Secure AI"),
        ("w3.org", "W3C"),
    ]
    for host_pat, vendor in vendor_map:
        if host == host_pat or host.endswith("." + host_pat):
            tail = humanise_segment(last) or humanise_segment(
                (path.strip("/").split("/")[-2] if "/" in path.strip("/") else path.strip("/"))
            )
            return f"{vendor}: {tail}" if tail else vendor

    # Pure-host fallback: prefix with the host so labels are at least
    # publisher-attributed.
    host_clean = host.removeprefix("www.")
    tail = humanise_segment(last) or humanise_segment(path.split("/")[-2] if "/" in path else "")
    if tail:
        return f"{host_clean}: {tail}"
    return host_clean or url


# ─────────────────────────────────────────────────────────────────────────
# Resolution: doc → curated source IDs + uncurated URLs
# ─────────────────────────────────────────────────────────────────────────


def get_doc_mapping(
    src_rel: str,
    mappings: dict[str, dict],
) -> dict[str, list[str]]:
    """Resolve a doc's citation mapping.

    Order:
    1. Exact full path match (e.g. ``docs/foo.md``).
    2. Short key match — ONLY for files under ``docs/`` — so legacy
       short keys like ``foo.md`` still resolve. This branch is
       intentionally NOT applied to repo-root files like
       ``README.md`` to keep the namespace disjoint.
    """
    if src_rel in mappings:
        return mappings[src_rel]
    if src_rel.startswith("docs/"):
        short = src_rel.removeprefix("docs/")
        if short in mappings:
            return mappings[short]
    return {}


def auto_resolve_keywords(
    body: str,
    keywords: dict[str, list[str]],
) -> list[str]:
    """Scan the doc for known terms and return matching source IDs."""
    hits: list[str] = []
    seen: set[str] = set()
    for term, source_ids in keywords.items():
        # Word-boundary, case-insensitive, but require the term to be
        # well-formed (no leading/trailing word chars). Some terms have
        # punctuation (e.g. "ISO/IEC 27001") so we don't \b around them.
        pat = re.escape(term)
        if re.search(rf"(?<![\w-]){pat}(?![\w-])", body, re.IGNORECASE):
            for sid in source_ids:
                if sid not in seen:
                    seen.add(sid)
                    hits.append(sid)
    return hits


def extract_additional_urls(
    body: str,
    covered_urls: set[str],
) -> "OrderedDict[str, str]":
    """URLs that appear in the body but aren't covered by curated sources.

    Returns an OrderedDict url → cleaned label, in first-appearance
    order, so the rendered list is stable.
    """
    found: "OrderedDict[str, str]" = OrderedDict()
    # First, markdown links (`[label](url)`).
    for m in LINK_RE.finditer(body):
        label = m.group(1).strip()
        url = normalize_url(m.group(2).strip())
        url_no_frag = urldefrag(url).url
        if url_excluded(url_no_frag):
            continue
        if url_no_frag in covered_urls:
            continue
        if url in found:
            continue
        # Drop labels that are URL-as-label or single-slug junk.
        url_only_label = label.startswith("http") or label == url
        single_slug = bool(re.fullmatch(r"[a-z0-9._-]{1,20}", label))
        # Markdown bold/italic noise.
        clean_label = re.sub(r"^[*_]+|[*_]+$", "", label).strip()
        if url_only_label or single_slug or not clean_label:
            found[url] = humanise_url(url)
        else:
            found[url] = clean_label
    # Then bare URLs.
    for m in URL_RE.finditer(body):
        url = normalize_url(m.group(0))
        url_no_frag = urldefrag(url).url
        if url_excluded(url_no_frag):
            continue
        if url_no_frag in covered_urls:
            continue
        if url in found:
            continue
        found[url] = humanise_url(url)
    return found


# ─────────────────────────────────────────────────────────────────────────
# Repo-internal graph (Related / Cited by)
# ─────────────────────────────────────────────────────────────────────────


def build_repo_graph(
    nodes: list[Path],
) -> tuple[dict[str, set[str]], dict[str, set[str]]]:
    by_path = {p.resolve(): p for p in nodes}
    in_links: dict[str, set[str]] = defaultdict(set)
    out_links: dict[str, set[str]] = defaultdict(set)

    def rel(p: Path) -> str:
        return str(p.relative_to(REPO)).replace(os.sep, "/")

    for src in nodes:
        raw = src.read_text(encoding="utf-8", errors="replace")
        text = strip_code_regions(strip_autogen_block(raw))
        for m in ANY_LINK_RE.finditer(text):
            raw_target = m.group(2).strip()
            target = raw_target.split("#", 1)[0]
            if not target or not target.endswith(".md"):
                continue
            if target.startswith(("http://", "https://", "mailto:",
                                  "file:", "//")):
                continue
            abs_t = (src.parent / target).resolve()
            if abs_t not in by_path:
                continue
            if abs_t == src.resolve():
                continue
            out_links[rel(src)].add(rel(by_path[abs_t]))
            in_links[rel(by_path[abs_t])].add(rel(by_path[src]))

    return out_links, in_links


