def _scan_protected_spans(text: str) -> list[tuple[int, int]]:
    """Return (start, end) byte spans we must NEVER inject into.

    Protected: fenced code blocks, inline code, ATX/Setext headings,
    HTML tags, YAML frontmatter, markdown link `[label](url)` body,
    URL of a link, and the auto-generated footer.
    """
    spans: list[tuple[int, int]] = []

    # Fenced code blocks
    for m in re.finditer(r"^[ \t]*```.*?\n.*?^[ \t]*```",
                         text, re.DOTALL | re.MULTILINE):
        spans.append((m.start(), m.end()))

    # Inline `code`
    for m in re.finditer(r"`[^`\n]*`", text):
        spans.append((m.start(), m.end()))

    # ATX headings
    for m in re.finditer(r"^[ \t]*#{1,6}.*$", text, re.MULTILINE):
        spans.append((m.start(), m.end()))

    # Setext underlines
    for m in re.finditer(r"^[ \t]*(=+|-+)[ \t]*$", text, re.MULTILINE):
        spans.append((m.start(), m.end()))

    # HTML tags (single line, conservative)
    for m in re.finditer(r"<[^<>\n]+>", text):
        spans.append((m.start(), m.end()))

    # YAML frontmatter
    if text.startswith("---\n"):
        m = re.match(r"---\n.*?\n---\n", text, re.DOTALL)
        if m:
            spans.append((m.start(), m.end()))

    # Existing markdown links — protect both label and URL.
    for m in ANY_LINK_RE.finditer(text):
        spans.append((m.start(), m.end()))

    # Reference-style link definitions `[id]: url`
    for m in re.finditer(r"^[ \t]*\[[^\]\n]+\]:[ \t]*\S+", text, re.MULTILINE):
        spans.append((m.start(), m.end()))

    # Our own footer
    for m in MARKER_BLOCK_RE.finditer(text):
        spans.append((m.start(), m.end()))

    spans.sort()
    return spans


def _in_protected(pos: int, spans: list[tuple[int, int]]) -> bool:
    # Binary-search style scan; spans is sorted.
    for s, e in spans:
        if pos < s:
            return False
        if s <= pos < e:
            return True
    return False


def _inline_marker_html(num: int) -> str:
    return f'<sup class="ref">[<a href="#ref-{num}">{num}</a>]</sup>'


def inject_inline_citations(
    body: str,
    id_to_num: dict[str, int],
    phrases: dict[str, list[str]],
) -> str:
    """Add inline `[N]` citation markers at the first mention of each
    cited source's phrase. Idempotent (caller is expected to have stripped
    pre-existing markers before this runs).
    """
    if not id_to_num:
        return body

    # Build the candidate (phrase, source_id) list, longest phrases first
    # so "ISO/IEC 27001:2022" beats "ISO 27001".
    candidates: list[tuple[str, str]] = []
    for sid in id_to_num:
        for phrase in phrases.get(sid, []):
            candidates.append((phrase, sid))
    candidates.sort(key=lambda x: -len(x[0]))

    protected_spans = _scan_protected_spans(body)

    annotated_ids: set[str] = set()
    # We do up to one insertion per source, at the first non-protected
    # match. Build a list of (insertion_position, source_id, num) then
    # apply them all at once to avoid index drift.
    insertions: list[tuple[int, str, int]] = []
    for phrase, sid in candidates:
        if sid in annotated_ids:
            continue
        # Case-sensitive, word-boundary anchored.
        pat = re.compile(
            r"(?<![\w])" + re.escape(phrase) + r"(?![\w])"
        )
        for m in pat.finditer(body):
            pos = m.start()
            if _in_protected(pos, protected_spans):
                continue
            # Position the marker AFTER the matched text.
            insertions.append((m.end(), sid, id_to_num[sid]))
            annotated_ids.add(sid)
            break

    if not insertions:
        return body

    insertions.sort(key=lambda x: x[0])
    parts: list[str] = []
    cursor = 0
    for end, _sid, num in insertions:
        parts.append(body[cursor:end])
        parts.append(_inline_marker_html(num))
        cursor = end
    parts.append(body[cursor:])
    return "".join(parts)


def apply_to_file(
    src_path: Path,
    src_rel: str,
    library: dict[str, dict],
    mappings: dict[str, dict],
    keywords: dict[str, list[str]],
    inline_phrases: dict[str, list[str]],
    out_links: set[str],
    in_links: set[str],
) -> tuple[str, str]:
    original = src_path.read_text(encoding="utf-8", errors="replace")

    block, id_to_num = render_section(
        src_rel, src_path, library, mappings, keywords,
        out_links, in_links,
    )

    # 1. Replace the footer block (or append if missing).
    if BEGIN_MARKER in original and END_MARKER in original:
        new = MARKER_BLOCK_RE.sub(lambda m: block, original, count=1)
    else:
        sep = "" if original.endswith("\n") else "\n"
        new = original + sep + "\n---\n\n" + block + "\n"

    # 2. Strip ALL pre-existing inline citation markers (idempotency).
    new = INLINE_CITE_RE.sub("", new)

    # 3. Re-inject inline markers using *current* per-doc ref numbering.
    new = inject_inline_citations(new, id_to_num, inline_phrases)

    return original, new


