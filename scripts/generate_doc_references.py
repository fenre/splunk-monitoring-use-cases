def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--check", action="store_true",
                        help="Exit 1 if any file would change.")
    parser.add_argument("--dry-run", action="store_true",
                        help="Show what would change without writing.")
    parser.add_argument("--only", default=None,
                        help="Only operate on files matching this glob pattern.")
    parser.add_argument("--validate-library", action="store_true",
                        help="Verify every mapping ID resolves to a record; "
                             "exit 1 on any dangling reference.")
    args = parser.parse_args(argv)

    library, library_accessed = load_library()
    _resolve_accessed_date(library_accessed)
    mappings, keywords = load_mappings()
    inline_phrases = load_inline_phrases()

    if args.validate_library:
        missing: list[tuple[str, str]] = []
        for doc, m in mappings.items():
            for bucket in ("primary", "supporting", "related"):
                for sid in m.get(bucket, []):
                    if sid not in library:
                        missing.append((doc, sid))
        for term, ids in keywords.items():
            for sid in ids:
                if sid not in library:
                    missing.append((f"_topic_keywords/{term}", sid))
        for sid in inline_phrases:
            if sid not in library:
                missing.append(("inline-citation-phrases.json", sid))
        if missing:
            print(f"FAIL: {len(missing)} dangling source IDs in source-mappings.json:")
            for doc, sid in missing[:30]:
                print(f"  {doc}  →  {sid}")
            return 1
        print(f"OK: {len(library)} source records, all mapping IDs resolve.")
        return 0

    nodes = collect_md_nodes()
    out_links_map, in_links_map = build_repo_graph(nodes)

    def rel_of(p: Path) -> str:
        return str(p.relative_to(REPO)).replace(os.sep, "/")

    if args.only:
        import fnmatch
        nodes = [p for p in nodes if fnmatch.fnmatch(rel_of(p), args.only)]

    changed = 0
    skipped = 0
    file_changes: list[str] = []
    for src in nodes:
        src_rel = rel_of(src)
        if src_rel in SKIP:
            skipped += 1
            continue
        outs = out_links_map.get(src_rel, set())
        ins = in_links_map.get(src_rel, set())
        original, new = apply_to_file(src, src_rel, library, mappings,
                                      keywords, inline_phrases, outs, ins)
        if original == new:
            continue
        changed += 1
        file_changes.append(src_rel)
        if not args.check and not args.dry_run:
            src.write_text(new, encoding="utf-8")

    if args.check:
        if changed:
            print(f"FAIL: {changed} files have stale auto-references.")
            for f in file_changes[:15]:
                print(f"  - {f}")
            if changed > 15:
                print(f"  … and {changed - 15} more.")
            print("Run scripts/generate_doc_references.py to refresh.")
            return 1
        print(f"OK: all {len(nodes)} docs have up-to-date auto-references.")
        return 0

    print(f"Processed {len(nodes)} files; "
          f"{changed} rewritten, {skipped} skipped, "
          f"{len(nodes) - changed - skipped} already current.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
