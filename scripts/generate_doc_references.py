#!/usr/bin/env python3
"""
generate_doc_references.py — emit Wikipedia/academic-class "References"
footers on every documentation node in this repository.

For each markdown node:

1. Pulls the curated source library from `data/source-references.json`
   (hand-maintained bibliographic records: authority, year, title,
   publisher, URL, type, edition, identifier).
2. Resolves the doc's primary/supporting citations via
   `data/source-mappings.json` (hand-curated per-doc), with a
   keyword-driven auto-fill for unmapped docs.
3. Cross-checks the doc's body for any bare or labelled URL not yet
   covered and emits cleaned, deduplicated "Additional online sources"
   entries (with sensible labels — no more "[ssae]" or "[contents]"
   garbage).
4. Renders a numbered, anchored reference list in APA-flavoured form:
       [1] Splunk Inc. (2026). *Splunk IT Service Intelligence
           Administration Manual.* Splunk LLC, a Cisco company.
           Retrieved May 11, 2026, from
           https://docs.splunk.com/Documentation/ITSI
5. Adds a small "Related repository documents" / "Cited by" block at
   the very end so cross-navigation survives.
6. Wraps everything in idempotent HTML markers so re-runs are byte-for-
   byte stable.

Usage:
    python3 scripts/generate_doc_references.py            # write
    python3 scripts/generate_doc_references.py --check    # exit 1 if stale
    python3 scripts/generate_doc_references.py --dry-run  # preview
    python3 scripts/generate_doc_references.py --only docs/foo.md
"""
