import argparse
import datetime as dt
import json
import os
import re
import sys
from collections import OrderedDict, defaultdict
from pathlib import Path
from urllib.parse import urlparse, urldefrag

REPO = Path(__file__).resolve().parent.parent

BEGIN_MARKER = "<!-- BEGIN-AUTOGENERATED-SOURCES -->"
END_MARKER = "<!-- END-AUTOGENERATED-SOURCES -->"
SECTION_HEADING = "## References"
SUBSECTION_PRIMARY = "### Primary sources"
SUBSECTION_SUPPORTING = "### Supporting sources"
SUBSECTION_ADDITIONAL = "### Additional online sources"
SUBSECTION_REPO_OUT = "### Related repository documents"
SUBSECTION_REPO_IN = "### Cited by"

# Limit how many auto-detected supporting refs we add per doc when no
# explicit mapping exists.
MAX_AUTO_SUPPORTING = 8
MAX_REPO_LINKS_PER_DIRECTION = 20

# Reproducibility: prefer the date stamped in source-references.json
# (_meta.accessedDate). Override with SOURCE_REFS_ACCESSED_DATE env var
# for ad-hoc reruns. Fall back to today's date in UTC.
ACCESSED_DATE_ENV = os.environ.get("SOURCE_REFS_ACCESSED_DATE")

LIBRARY_PATH = REPO / "data" / "source-references.json"
MAPPINGS_PATH = REPO / "data" / "source-mappings.json"
INLINE_PHRASES_PATH = REPO / "data" / "inline-citation-phrases.json"

# Pages that should NOT receive an auto-generated footer because some
# other script fully regenerates them on every build, which would
