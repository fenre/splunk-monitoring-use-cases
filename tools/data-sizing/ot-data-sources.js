// Helper: get unique categories in order
function getCategories() {
  const seen = new Set();
  return OT_DATA_SOURCES.reduce((acc, s) => {
    if (!seen.has(s.category)) { seen.add(s.category); acc.push(s.category); }
    return acc;
  }, []);
}

// Helper: get sources for a category
function getSourcesByCategory(category) {
  return OT_DATA_SOURCES.filter(s => s.category === category);
}

// Helper: recommend license tier
function recommendLicenseTier(totalGBPerDay) {
  for (const tier of SPLUNK_LICENSE_TIERS) {
    if (totalGBPerDay <= tier.gb_per_day) return tier;
  }
  return SPLUNK_LICENSE_TIERS[SPLUNK_LICENSE_TIERS.length - 1];
}
